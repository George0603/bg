package com.bg.model;

import java.util.Date;

public class BabyData {
	
	public String babyname;
	public Date date;
	public float height;
	public float weight;
	public float bmi;
	public float height_weight;
	public float head;
	public float seat;
	public float bust;
	
	public String getBabyname() {
		return babyname;
	}
	public void setBabyname(String babyname) {
		this.babyname = babyname;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public float getHeight() {
		return height;
	}
	public void setHeight(float height) {
		this.height = height;
	}
	public float getWeight() {
		return weight;
	}
	public void setWeight(float weight) {
		this.weight = weight;
	}
	public float getBmi() {
		return bmi;
	}
	public void setBmi(float bmi) {
		this.bmi = bmi;
	}
	public float getHeight_weight() {
		return height_weight;
	}
	public void setHeight_weight(float height_weight) {
		this.height_weight = height_weight;
	}
	public float getHead() {
		return head;
	}
	public void setHead(float head) {
		this.head = head;
	}
	public float getSeat() {
		return seat;
	}
	public void setSeat(float seat) {
		this.seat = seat;
	}
	public float getBust() {
		return bust;
	}
	public void setBust(float bust) {
		this.bust = bust;
	}
}
